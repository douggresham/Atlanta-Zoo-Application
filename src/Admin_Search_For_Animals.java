    public void fillValues() {
       
            
        
            Connection con = null;
            try {
                Class.forName("com.mysql.jdbc.Driver").newInstance();
                con = DriverManager.getConnection("jdbc:mysql://academic-mysql.cc.gatech.edu/cs4400_group8",
                "cs4400_group8",
                "l5sU95me");
                PreparedStatement stmt;
                String sql = "SELECT * FROM `Animals`";
                
                stmt = con.prepareStatement(sql);
                
                //
                //System.out.println(sql);
                ResultSet myRs = stmt.executeQuery();
                //clear table
                for (int i = 0; i < 25; i++) {
                    for (int j = 0; j < 5; j++) {
                        jTable.getModel().setValueAt(null, i, j);   
                    }
                }
                int i = 0;
                Object value = null;
                while (myRs.next()) {
                    for (int j = 0; j < 5; j++) {
                        if (j == 0) {
                            value = myRs.getString("Name");
                        } else if (j == 1) {
                            value = myRs.getString("Species");
                        } else if (j == 2) {
                            value = myRs.getString("ExhibitName");
                        } else if (j == 3){
                            value = myRs.getInt("Age");
                        } else {
                            value = myRs.getString("AnimalType");
                        }
                        jTable.getModel().setValueAt(value, i, j);
                    }
                    i++; //move to next result
                }
            } catch(Exception e) {
                System.err.println("Exception: " + e.getMessage());
            } finally {
                try {
                    if(con != null)
                    con.close();
                } catch(SQLException e) {}
            } 
        }
    
